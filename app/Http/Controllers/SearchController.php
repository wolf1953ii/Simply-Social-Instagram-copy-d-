     * Display a listing of the resource.
     *
     * @return \Illuminate\Http\Response
     */
    public function index(Request $request)
    {
        if(Auth::check()){
            $images = Image::select('title','name','id','privacy')
                ->where('privacy',0)->where('title','like','%'.$request->input('searchFor').'%')->get();
            return view('searchResults',compact('images'));
        }
        return Redirect::to("main")->with('message','Opps! You do not have access');
    }

